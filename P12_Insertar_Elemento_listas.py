######          append()
#append() Agrega un elemento al final de la lista
print("\tMetodo de append()")

colores = ['rojo', 'azul', 'verde', 'amarillo', 'marron', 'lila', 'negro', 'rosa', 'blanco', 'naranja']
print(colores)

colores.append('fuxia')
print(colores)

colores.append('celeste')
print(colores)

######          insert()
#insert() Agrega un elemento en la posicion que especifiques
print("\n\tMetodo de insert()")

colores = ['rojo', 'azul', 'verde', 'amarillo', 'marron', 'lila', 'negro', 'rosa', 'blanco', 'naranja']
print(colores)

colores.insert(6,'magenta')
print(colores)

colores.insert(10,'turquesa')
print(colores)