#While es bucle que te perminte repetir un proceso hasta que se cumpla una condicion
print('\tPrimer bucle\n')
x = 0

while x <= 15:
	print(x)
	x+=5

print('\tSegundo bucle\n')
x = -100

while x <= 0:
	print(x)
	x-=-20
	
print('\tTercer bucle\n')
x = 10

while x >= 0:
	print('El valor del bucle es: ', x)
	x-=1